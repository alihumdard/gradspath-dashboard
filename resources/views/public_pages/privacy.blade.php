  <h1>Privacy Policy</h1>
  <p class="lede">This Privacy Policy explains how Grads Paths collects, uses, shares, and protects personal information, including information used to schedule and host Zoom meetings.</p>

  <h2>Information We Collect</h2>
  <p>We collect information users provide directly, such as name, email address, profile information, educational or professional background, booking details, messages, payment-related records, support requests, and mentor availability.</p>
  <p>When mentors connect Zoom, we store Zoom OAuth tokens and related account identifiers needed to create and manage meetings. For bookings, we may store Zoom meeting identifiers, join links, meeting status, and attendance-related webhook events.</p>

  <h2>How We Use Information</h2>
  <p>We use personal information to operate Grads Paths, authenticate users, show mentor profiles, process bookings, create virtual meeting links, manage Office Hours, process payments and payouts, provide support, maintain security, and comply with legal obligations.</p>

  <h2>How We Share Information</h2>
  <p>We do not sell personal information. We share information with service providers only as needed to operate the platform, including hosting providers, payment processors, email providers, analytics tools, and Zoom for meeting creation and attendance workflows.</p>

  <h2>Zoom Data Use</h2>
  <p>Zoom data is used to connect mentor Zoom accounts, create meetings for confirmed bookings, display join or start links, receive meeting event notifications, and support attendance or session completion workflows. Users can disconnect Zoom from their account settings or contact support for help.</p>

  <h2>Data Subject Rights</h2>
  <p>Depending on where you live, you may have rights to access, correct, delete, restrict, or receive a copy of personal information we hold about you. You may also have the right to object to certain processing or withdraw consent where processing is based on consent.</p>
  <p>To exercise these rights, email <a href="mailto:support@gradspaths.com">support@gradspaths.com</a>. We may need to verify your identity before completing a request. Some records may be retained when required for security, fraud prevention, tax, accounting, legal, dispute resolution, or platform integrity reasons.</p>

  <h2>Security and Retention</h2>
  <p>We use reasonable administrative, technical, and organizational safeguards to protect information. We retain information for as long as needed to provide services, meet legal obligations, resolve disputes, and maintain platform security.</p>

  <h2>Children</h2>
  <p>Grads Paths is not intended for children under 13. Users under 18 may need consent from a parent or guardian where required by law.</p>

  <h2>Contact</h2>
